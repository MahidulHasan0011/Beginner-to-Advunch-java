
public class AssignmentOperator {
    public static void main (String[]args){
    int x,y;
    x=3;
    y=2;
    x+=y; //x=x+y=30
    System.out.println("x+= is "+x);
    
      x-=y; //x=x-y=30
    System.out.println("x-= is "+x);
    
    
      x*=y; //x=x*y=30
    System.out.println("x*= is "+x);
    
      x/=y; //x=x/y=30
    System.out.println("x/= is "+x);
    
      x%=y; //x=x%y=30
    System.out.println("x%= is "+x);
    
    
    }
}
